export enum LazyLoadState {
    NONE = 0, RUNNING, DONE
}
import { Injectable } from '@angular/core';

@Injectable()
export class LazyLoadService {



    constructor() {

    }

    loadImage(_url) {
        let img = new Image();
        img.src = _url;
    }
    loadImages() {
        let target = this;
        Object.keys(assets).forEach((key) => {
            let imgs = assets[key];
            for (let imgUrl of imgs) {
                target.loadImage(imgUrl);
            }
        });
    }
}


